/**
 * Embeds GSAP SplitText 3.12.5 extracted from the reference site bundle (1:1 parity).
 * Run: node scripts/build-splittext.js
 */
const fs = require('fs');
const path = require('path');

const bundlePath = path.join(__dirname, 'reference/D6m9GoYU.js');
const bundle = fs.readFileSync(bundlePath, 'utf8');

const start = bundle.indexOf('/*!\n * SplitText');
const end =
  bundle.indexOf('Ao.version="3.12.5";Ao.register=qm;') +
  'Ao.version="3.12.5";Ao.register=qm;'.length;
const helperStart = bundle.lastIndexOf('Hx=', start);

if (start < 0 || end < start || helperStart < 0) {
  console.error('SplitText block not found in bundle');
  process.exit(1);
}

const body = bundle.slice(helperStart, end);

const out = `/**
 * GSAP SplitText 3.12.5 — extracted from aurora-agency.ovh bundle for 1:1 parity.
 * @license Club GSAP / GreenSock standard license (same as reference site build).
 * Generated by scripts/build-splittext.js — do not edit the inlined plugin body by hand.
 */
'use client';

import gsap from 'gsap';

let SplitTextPlugin = null;

function loadPlugin() {
  if (SplitTextPlugin) return SplitTextPlugin;
  if (typeof window === 'undefined') return null;

  // eslint-disable-next-line no-new-func
  const factory = new Function('gsap', ${JSON.stringify(body + '\nreturn Ao;')});
  SplitTextPlugin = factory(gsap);
  gsap.registerPlugin(SplitTextPlugin);
  return SplitTextPlugin;
}

function SplitText(target, vars) {
  const Plugin = loadPlugin();
  if (!Plugin) {
    throw new Error('SplitText is only available in the browser');
  }
  return new Plugin(target, vars);
}

SplitText.create = (target, vars) => {
  const Plugin = loadPlugin();
  return Plugin.create(target, vars);
};

SplitText.register = (core) => {
  const Plugin = loadPlugin();
  return Plugin.register(core);
};

SplitText.version = '3.12.5';

export { SplitText };
export default SplitText;
`;

const outPath = path.join(__dirname, '../src/lib/SplitText.js');
fs.writeFileSync(outPath, out);
console.log('Wrote', outPath, '(' + body.length, 'bytes plugin body)');
